cc.Class({
    extends: cc.Component,

    properties: {
        slotPrefab: {
            default: null,
            type: cc.Prefab
        },
        scrollView: {
            default: null,
            type: cc.ScrollView
        },
        totalCount: 0
    },
 
    // LIFE-CYCLE CALLBACKS:

    // onLoad () {},

    start () {
        for (let i = 0; i < 10; i++) {
            let chatSlot = cc.instantiate(this.slotPrefab);
            this.scrollView.content.addChild(chatSlot);
        }

    },

    // update (dt) {},
});
